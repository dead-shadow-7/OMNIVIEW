function performSearch() {
  const query = searchInput.value.trim();
  if (!query) {
    addLog("error", "Please enter a search query.");
    return;
  }
  isSearching = !0;
  showNewsResults();
  showLoading();
  const whenForRequest = currentWhen === "all" ? null : currentWhen;
  fetch(API_CONFIG.getUrl("NEWS"), {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ query: query, section: currentSection, when: whenForRequest }),
  })
    .then((r) => r.json())
    .then((data) => {
      displayResults(data);
      addLog(
        "info",
        `Fetched ${data.articles.length} articles for "${query}" (${WHEN_LABEL[currentWhen]})`
      );
      if (data.articles && data.articles.length) {
        fetchDisasterBrief(query, data.articles);
      } else {
        hideBrief();
      }
    })
    .catch((err) => {
      showError("Error fetching news. Please try again.");
      addLog("error", "Error fetching news: " + err.message);
      console.error("Search error:", err);
    });
}

function fetchDisasterBrief(query, articles) {
  showBriefLoading();
  fetch(API_CONFIG.getUrl("NEWS_BRIEF"), {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ query: query, articles: articles }),
  })
    .then((r) => r.json())
    .then((data) => {
      if (data.brief) {
        renderBrief(data.brief);
      } else {
        hideBrief();
        addLog("warning", "Disaster brief unavailable: " + (data.error || "unknown"));
      }
    })
    .catch((err) => {
      hideBrief();
      addLog("error", "Brief generation failed: " + err.message);
    });
}

